CREATE TABLE `tblDeal` (
	`DealID` BIGINT(20) UNSIGNED NOT NULL DEFAULT '0',
	`Title` VARCHAR(250) NOT NULL DEFAULT '' COLLATE 'utf8_unicode_ci',
	`DealType` VARCHAR(50) NOT NULL DEFAULT 'Revenue' COLLATE 'utf8_unicode_ci',
	`AccountID` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`CodedeckID` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`AlertEmail` VARCHAR(250) NULL DEFAULT '' COLLATE 'utf8_unicode_ci',
	`StartDate` DATETIME NULL DEFAULT NULL,
	`EndDate` DATETIME NULL DEFAULT NULL,
	`TotalPL` DECIMAL(18,6) NOT NULL DEFAULT '0.000000',
	`created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	`CreatedBy` VARCHAR(100) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	`updated_at` DATETIME NULL DEFAULT NULL,
	`ModifiedBy` VARCHAR(100) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	PRIMARY KEY (`DealID`)
)
COLLATE='utf8_unicode_ci'
ENGINE=InnoDB
;


CREATE TABLE `tblDealDetail` (
	`DealNoteID` BIGINT(20) NOT NULL AUTO_INCREMENT,
	`DealID` BIGINT(20) UNSIGNED NOT NULL DEFAULT '0',
	`DestinationCountryID` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`TrunkID` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`Revenue` DECIMAL(18,6) UNSIGNED NULL DEFAULT '0.000000',
	`SalePrice` DECIMAL(18,6) UNSIGNED NOT NULL DEFAULT '0.000000',
	`BuyPrice` DECIMAL(18,6) UNSIGNED NOT NULL DEFAULT '0.000000',
	`Minutes` DECIMAL(18,6) UNSIGNED NOT NULL DEFAULT '0.000000',
	`PerMinutePL` DECIMAL(18,6) UNSIGNED NOT NULL DEFAULT '0.000000',
	`TotalPL` DECIMAL(18,6) UNSIGNED NOT NULL DEFAULT '0.000000',
	`created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	`updated_at` DATETIME NULL DEFAULT NULL,
	PRIMARY KEY (`DealNoteID`)
)
COLLATE='utf8_unicode_ci'
ENGINE=InnoDB
ROW_FORMAT=DYNAMIC
;

CREATE TABLE `tblDealNote` (
	`DealNoteID` BIGINT(20) NOT NULL AUTO_INCREMENT,
	`DealID` BIGINT(20) UNSIGNED NOT NULL DEFAULT '0',
	`Note` TEXT NULL COLLATE 'utf8_unicode_ci',
	`created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	`CreatedBy` VARCHAR(100) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	`updated_at` DATETIME NULL DEFAULT NULL,
	`ModifiedBy` VARCHAR(100) NULL DEFAULT NULL COLLATE 'utf8_unicode_ci',
	PRIMARY KEY (`DealNoteID`)
)
COLLATE='utf8_unicode_ci'
ENGINE=InnoDB
;

